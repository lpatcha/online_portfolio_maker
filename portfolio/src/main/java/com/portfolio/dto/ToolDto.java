package com.portfolio.dto;

public class ToolDto {
	String description;
	int proficencyRating;
	int userId;
	int id;
	int toolId;
	
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public int getProficencyRating() {
		return proficencyRating;
	}
	public void setProficencyRating(int proficencyRating) {
		this.proficencyRating = proficencyRating;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getToolId() {
		return toolId;
	}
	public void setToolId(int toolId) {
		this.toolId = toolId;
	}
	
}
